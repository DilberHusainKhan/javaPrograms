public class printStatement {
    public static void main(String[] args) {
        System.out.println("Difference Between println and print");
        System.out.print("Hello World");
        System.out.print("Hello World");
        System.out.print("\n");
        System.out.println("Hello World");
        System.out.println("Hello World");

        // To Draw Pattern
        System.out.println("Pattern Drawing using five Print Statements");
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");

        System.out.println("Pattern Using Single Statement");
        System.out.print("*\n* *\n* * *\n* * * *\n* * * * *\n");

        // Equilateral Triangle
        System.out.println("Pattern Using MultiLine Statement");
        System.out.println("   *    \n  ***  \n ***** \n*******");

        // Print Z Pattern
        System.out.println("Z-Pattern Using Single Statement");
        System.out.print("*****\n   * \n  *  \n *   \n*****");

    }

}
